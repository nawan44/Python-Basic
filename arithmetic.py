
#Standart Operator Arithmetic
a = 10
b  = 3

#operator +
res  = a + b
print(a, '+', b ,'=', res)

#operator -
res  = a - b
print(a, '-', b ,'=', res)

#operator - Multiple
res  = a * b
print(a, '-', b ,'=', res)

# operator / Dividen
res  = a / b
print(a, '/', b ,'=', res)

# operator ^2 Eksponen
res  = a ** b
print(a, '**', b ,'=', res)

# operator % Modulus
res  = a % b
print(a, '%', b ,'=', res)

# operator // floor division
res  = a // b
print(a, '//', b ,'=', res)

